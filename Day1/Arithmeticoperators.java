//Write a Java program to demonstrate the use of arithmetic operators.
import java.util.*;
class Arithmeticoperators {
    public static void main(String[] args) {
    Scanner s=new Scanner(System.in);
    
    int a=s.nextInt();
    int b=s.nextInt();
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);
}
}
